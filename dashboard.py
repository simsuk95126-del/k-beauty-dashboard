def initialize_session_state() -> None:
    defaults = {
        "free_analysis_units_left": 3,
        "current_auth_msg": None,
        "current_auth_tier": "INVALID",
        "last_entered_key": None,
        "disclaimer_agreed": False,
        "analysis_result": None,
        "localized_outputs": {},
        "report_build_id": REPORT_BUILD_ID,
    }
    for key, value in defaults.items():
        if key not in st.session_state:
            st.session_state[key] = value

    # A report-builder update must never reuse Word/Excel bytes generated by
    # an older session. Keep the analysis result, but invalidate old downloads.
    if st.session_state.get("report_build_id") != REPORT_BUILD_ID:
        st.session_state.localized_outputs = {}
        st.session_state.report_build_id = REPORT_BUILD_ID


def reset_results() -> None:
    st.session_state.analysis_result = None
    st.session_state.localized_outputs = {}


