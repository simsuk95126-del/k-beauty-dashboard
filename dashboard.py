@st.cache_data(show_spinner=False, ttl=3600)
def extract_ingredients_from_image(
    file_bytes: bytes,
) -> Tuple[List[str], Optional[str]]:
    if client is None:
        return [], "OPENAI_API_KEY가 설정되지 않았습니다."

    base64_image = base64.b64encode(file_bytes).decode("utf-8")
    prompt = (
        "화장품 전성분 이름을 원래 순서대로 추출하세요. "
        "성분 사이는 반드시 세로줄(|)로 구분하고 다른 설명은 쓰지 마세요. "
        "쉼표는 성분명 내부에 포함될 수 있으므로 구분자로 사용하지 마세요."
    )

    try:
        response = client.chat.completions.create(
            model="gpt-4o",
            messages=[
                {
                    "role": "user",
                    "content": [
                        {"type": "text", "text": prompt},
                        {
                            "type": "image_url",
                            "image_url": {
                                "url": (
                                    "data:image/jpeg;base64,"
                                    f"{base64_image}"
                                )
                            },
                        },
                    ],
                }
            ],
            temperature=0.0,
        )
        content = response.choices[0].message.content or ""
        values = [clean_text(value) for value in content.split("|")]
        return dedupe_preserve(values), None
    except Exception as exc:
        return [], f"이미지 성분 추출 실패: {type(exc).__name__}: {exc}"



@st.cache_data(show_spinner=False, ttl=86400)
def translate_report_texts(
    texts: Tuple[str, ...],
    language_code: str,
    protected_tokens: Tuple[str, ...] = (),
) -> Dict[str, str]:
    """보고서·Excel의 표시 문구를 한 번에 번역한다.

    규제 의미를 바꾸지 않고 토큰, INCI, CAS, 시장 코드, URL과 파일명은
    그대로 유지한다. 분석은 다시 실행하지 않으며 출력 파일만 현지화한다.
    """
    unique_texts = tuple(dedupe_preserve(texts))
    if not unique_texts:
        return {}
    if language_code not in OUTPUT_LANGUAGES:
        raise ValueError(f"지원하지 않는 출력 언어입니다: {language_code}")
    if client is None:
        raise RuntimeError(
            "다국어 파일 생성을 위한 OPENAI_API_KEY가 설정되지 않았습니다."
        )

    target_language = OUTPUT_LANGUAGES[language_code]
    result: Dict[str, str] = {}
    chunks: List[List[str]] = []
    current: List[str] = []
    current_chars = 0
    for text in unique_texts:
        if current and (len(current) >= 45 or current_chars + len(text) > 9000):
            chunks.append(current)
            current = []
            current_chars = 0
        current.append(text)
        current_chars += len(text)
    if current:
        chunks.append(current)

    for chunk in chunks:
        payload = [
            {"id": index, "source": text}
            for index, text in enumerate(chunk)
        ]
        glossary_prompt = TRANSLATION_GLOSSARY_PROMPTS.get(language_code, "")
        terminology_instruction = (
            f"\n\n{glossary_prompt}" if glossary_prompt else ""
        )
        if language_code == "en":
            style_instruction = (
                "Write clear, natural, professional regulatory English. "
                "Use short direct sentences and plain business language. "
                "Do not translate Korean word order literally. Avoid bureaucratic labels, "
                "vague phrases, and internal-system terminology. "
            )
        else:
            style_instruction = (
                "The source text is polished English. Translate it into natural, concise, "
                f"professional {target_language}. Do not translate word-for-word. "
                "Use short sentences that a business reader can understand immediately. "
                "Do not expand the text or reintroduce technical wording that was removed. "
            )
        system_prompt = (
            "You are a professional cosmetics regulatory document translator. "
            f"Translate every source string into {target_language}. "
            f"{style_instruction}"
            "Do not add legal conclusions, explanations, or unsupported claims. "
            "Preserve the exact meaning, warnings, numbering, and all placeholder tokens. "
            "Keep INCI names, CAS numbers, URLs, file names, market codes, institution "
            "acronyms, and official identifiers unchanged. Preserve question-and-answer "
            "structure where present. Return JSON only in the form "
            '{"translations":[{"id":0,"translation":"..."}]}. '
            "Placeholder tokens matching [[P####]] must remain exact."
            f"{terminology_instruction}"
        )
        try:
            response = client.chat.completions.create(
                model="gpt-4o",
                messages=[
                    {"role": "system", "content": system_prompt},
                    {
                        "role": "user",
                        "content": json.dumps(
                            {"items": payload},
                            ensure_ascii=False,
                        ),
                    },
                ],
                temperature=0.0,
            )
            content = (response.choices[0].message.content or "").strip()
            if content.startswith("```"):
                content = content.strip("`")
                if content.lower().startswith("json"):
                    content = content[4:].lstrip()
            parsed = json.loads(content)
            items = parsed.get("translations") or []
            by_id = {
                int(item["id"]): clean_text(item.get("translation"))
                for item in items
                if isinstance(item, dict) and "id" in item
            }
            for index, source in enumerate(chunk):
                translated = by_id.get(index)
                if not translated:
                    raise ValueError(f"번역 누락: {source[:80]}")
                result[source] = translated
        except Exception as exc:
            raise RuntimeError(
                f"{target_language} 출력 번역 실패: {type(exc).__name__}: {exc}"
            ) from exc
    return result


def report_translator(
    texts: Sequence[str],
    language_code: str,
    protected_tokens: Sequence[str],
) -> Dict[str, str]:
    return translate_report_texts(
        tuple(texts),
        language_code,
        tuple(protected_tokens),
    )


def dedupe_preserve(values: Sequence[str]) -> List[str]:
    result: List[str] = []
    seen: set[str] = set()
    for value in values:
        text = clean_text(value)
        key = text.casefold()
        if text and key not in seen:
            seen.add(key)
            result.append(text)
    return result


def read_uploaded_table(uploaded_file: Any) -> pd.DataFrame:
    extension = uploaded_file.name.rsplit(".", 1)[-1].casefold()
    raw_bytes = uploaded_file.getvalue()

    if extension in {"xlsx", "xls"}:
        return pd.read_excel(io.BytesIO(raw_bytes))

    encodings = ("utf-8-sig", "utf-8", "cp949", "euc-kr")
    for encoding in encodings:
        try:
            return pd.read_csv(
                io.BytesIO(raw_bytes),
                encoding=encoding,
            )
        except UnicodeDecodeError:
            continue

    return pd.read_csv(
        io.BytesIO(raw_bytes),
        encoding="utf-8",
        encoding_errors="replace",
    )


def extract_product(uploaded_file: Any) -> dict:
    source_file = uploaded_file.name
    base_name = Path(source_file).stem
    product_name = clean_text(base_name, "제품")
    extension = source_file.rsplit(".", 1)[-1].casefold()

    if extension in {"jpg", "jpeg", "png"}:
        ingredients, error = extract_ingredients_from_image(
            uploaded_file.getvalue()
        )
        return {
            "product_name": product_name,
            "source_file": source_file,
            "ingredients": ingredients,
            "preview": pd.DataFrame({"성분명": ingredients[:20]}),
            "error": error,
        }

    try:
        dataframe = read_uploaded_table(uploaded_file)
    except Exception as exc:
        return {
            "product_name": product_name,
            "source_file": source_file,
            "ingredients": [],
            "preview": pd.DataFrame(),
            "error": f"파일 읽기 실패: {type(exc).__name__}: {exc}",
        }

    if dataframe.empty or dataframe.shape[1] == 0:
        return {
            "product_name": product_name,
            "source_file": source_file,
            "ingredients": [],
            "preview": dataframe,
            "error": "읽을 수 있는 성분 행이 없습니다.",
        }

    ingredients = dedupe_preserve(
        dataframe.iloc[:, 0]
        .dropna()
        .map(clean_text)
        .tolist()
    )
    return {
        "product_name": product_name,
        "source_file": source_file,
        "ingredients": ingredients,
        "preview": dataframe.head(10),
        "error": None if ingredients else "유효한 성분이 없습니다.",
    }


